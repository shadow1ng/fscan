// =============================================================================
// 哈希验证常量
// =============================================================================

const (
	// HashRegexPattern MD5哈希正则表达式（32位十六进制）
	HashRegexPattern = `^[a-fA-F0-9]{32}$`
)

// =============================================================================
// 预编译正则表达式
// =============================================================================

var (
	// CompiledHashRegex 预编译的MD5哈希正则
	CompiledHashRegex *regexp.Regexp
)

func init() {
	CompiledHashRegex = regexp.MustCompile(HashRegexPattern)
}
